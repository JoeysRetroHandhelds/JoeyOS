package com.joeyos.app.data

// Represents an installed Android app that can be assigned as an emulator
data class InstalledApp(
    val packageName: String,
    val label: String
)

// A single retro system entry
data class RetroSystem(
    val id: String,
    val label: String,        // Short code shown in the dock tile
    val fullName: String,     // Full name shown in settings
    val category: String,
    val colorStart: Long,     // Gradient start (ARGB)
    val colorEnd: Long,       // Gradient end (ARGB)
    // Known package names for this system, in priority order (first installed wins for auto-assign)
    val knownPackages: List<String> = emptyList(),
    // When true the emulator dropdown shows ONLY knownPackages matches, no fallback to all apps
    val restrictToKnown: Boolean = false,
    // RetroArch cores supported for this system, in priority order (first = default)
    val retroarchCores: List<String> = emptyList()
)

